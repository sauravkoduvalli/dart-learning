/* --- Ternary Operator --- */

/*
  - It is a simplified for or a one-liner replacement of if-else statement.
  - It is used to write conditional expression, where based on the result of boolean condition, one of the two value is selected.
  - Syntax: condition? value1: value2;
*/

void main() {
  final A = 9;
  final B = 20;

  A>B ? print('A is grater') : print('B is grater');
}
